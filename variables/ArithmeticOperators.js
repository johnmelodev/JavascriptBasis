// + - / * %

// Addition
let addition = 5 + 3;
console.log("The result is: " + addition);

// Subtraction
let subtraction = 5 - 3;
console.log("The result is: " + subtraction);

// Multiplication
let multiplication = 5 * 3;
console.log("The result is: " + multiplication);

// Division
let division = 15 / 3;
console.log("The result is: " + division);

// Modulo (remainder of division)
let modulo = 10 % 3;
console.log("The result is: " + modulo);
