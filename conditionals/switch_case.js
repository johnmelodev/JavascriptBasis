let age = Number(prompt('Enter your age'));

switch (age) {
    case 10:
        console.log('You are 10 years old.');
        break;
    case 15:
        console.log('You are 15 years old.');
        break;
    case 20:
        console.log('You are 20 years old.');
        break;
    default:
        console.log('Invalid age.');
        break;
}