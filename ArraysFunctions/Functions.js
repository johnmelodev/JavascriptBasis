// Declaring the list of numbers
let numbers = [1, 2, 3, 4, 5];

// Demonstration of the indexOf function to find the index of an element
console.log("numbers.indexOf(6)");
console.log(numbers.indexOf(6));

// Demonstration of the includes function to check if an element is present in the list
console.log("numbers.includes(6)");
console.log(numbers.includes(6));

console.log("numbers.includes(4)");
console.log(numbers.includes(4));

// Displaying the list of numbers
console.log("numbers");
console.log(numbers);

// Demonstration of the push function to add an element to the end of the list
console.log("numbers.push(8)");
console.log(numbers.push(8));
