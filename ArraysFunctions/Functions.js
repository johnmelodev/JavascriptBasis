// Declarando a lista de números
let numeros = [1, 2, 3, 4, 5];

// Demonstração da função indexOf para encontrar o índice de um elemento
console.log("numeros.indexOf(6)");
console.log(numeros.indexOf(6));

// Demonstração da função includes para verificar se um elemento está presente na lista
console.log("numeros.includes(6)");
console.log(numeros.includes(6));

console.log("numeros.includes(4)");
console.log(numeros.includes(4));

// Exibindo a lista de números
console.log("numeros");
console.log(numeros);

// Demonstração da função push para adicionar um elemento ao final da lista
console.log("numeros.push(8)");
console.log(numeros.push(8));